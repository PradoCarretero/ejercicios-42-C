#include<unistd.h>
#include<stdio.h>

int	ft_recursive_power(int nb, int power)
{
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (power > 0)
		return (nb * ft_recursive_power(nb, power - 1));
	return (1);
}

